# String literals
print("This uses double quotes.")
print('This uses single quotes.')

# Multi-line strings
print("""This uses triple double quotes,
which allows the string to span over multiple lines.""")
print('''Both this and the previous are
called multi-line strings.''')

# Multi-line Strings preserve spacing
print('''The next line starts with a tab.
    This is tabbed!''')

# Escape Sequences
print("hello\tgoodbye")
print("You say, \
\"I\'d like to buy a hamburger\\cheeseburger.\"")
print("How\nmany\nlines\ndoes\nthis\nspan?")